package com.comparadorad.bet.comparer.model.config.bean.mapreduce;

import org.springframework.data.annotation.Id;

/**
 * The Class AbstractBeanMapReduce.
 */
public abstract class AbstractBeanMapReduce {

	/** The id. */
	@Id
	private String id;

	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 * 
	 * @param id
	 *            the new id
	 */
	public void setId(String id) {
		this.id = id;
	}
}
